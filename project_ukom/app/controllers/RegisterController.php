	/**
	 * Store a newly created resource in storage.
	 *
	 * @return Response
	 */
	public function store()
	{
		$rules = array(
        'first' => 'required',
        'last' => 'required',
        'email' => 'required',
        'password' => 'required'
        );
    
        $validation = Validator::make(Input::all(), $rules);
        if($validation->fails()) {
        return Redirect::back()->withInput()->withErrors($validation->messages());
        }
        else{
        $regis = new Regis;

		$regis->first = Input::get('first');
		$regis->last = Input::get('last');
		$regis->email = Input::get('email');
		$regis->password = Input::get('password');

		$regis->save();

		return Redirect::to('vila');
        }
	}


